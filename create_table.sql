CREATE TABLE sales (

Order_ID VARCHAR(20),

Order_Date DATE,

Ship_Date DATE,

Customer_ID VARCHAR(20),

Customer_Name VARCHAR(100),

State VARCHAR(50),

City VARCHAR(50),

Region VARCHAR(50),

Category VARCHAR(50),

Sub_Category VARCHAR(50),

Product_Name VARCHAR(200),

Sales DECIMAL(10,2),

Quantity INT,

Discount DECIMAL(5,2),

Profit DECIMAL(10,2),

Shipping_Cost DECIMAL(10,2),

Payment_Method VARCHAR(50),

Rating DECIMAL(3,1)

);
